#include "libft.h"

void	*ft_memset(void *ptr, int value, size_t num)
{
	size_t		i;
	char		*c;

	c = ptr;
	i = 0;
	while (i < num)
	{
		*(c++) = value;
		i++;
	}
	return (ptr);
}
