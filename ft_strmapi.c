#include "libft.h"

char		*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		i;

	if (!s || !f)
		return (ft_strdup(""));
	if (!(str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1))))
		return (ft_strdup(""));
	i = 0;
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
